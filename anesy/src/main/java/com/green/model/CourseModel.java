package com.green.model;

public class CourseModel {

	private String avatar;
	private String name;
	private String shortdesc;
	private String description;
	private int price;
	private String benefit;
	private String requiment;
	private String include;
	private String action;
	
	

	/**
	 * @return the action
	 */
	public String getAction() {
		return action;
	}
	/**
	 * @param action the action to set
	 */
	public void setAction(String action) {
		this.action = action;
	}
	/**
	 * @return the avatar
	 */
	public String getAvatar() {
		return avatar;
	}
	/**
	 * @param avatar the avatar to set
	 */
	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the shortdesc
	 */
	public String getShortdesc() {
		return shortdesc;
	}
	/**
	 * @param shortdesc the shortdesc to set
	 */
	public void setShortdesc(String shortdesc) {
		this.shortdesc = shortdesc;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the price
	 */
	public int getPrice() {
		return price;
	}
	/**
	 * @param price the price to set
	 */
	public void setPrice(int price) {
		this.price = price;
	}
	/**
	 * @return the benefit
	 */
	public String getBenefit() {
		return benefit;
	}
	/**
	 * @param benefit the benefit to set
	 */
	public void setBenefit(String benefit) {
		this.benefit = benefit;
	}
	/**
	 * @return the requiment
	 */
	public String getRequiment() {
		return requiment;
	}
	/**
	 * @param requiment the requiment to set
	 */
	public void setRequiment(String requiment) {
		this.requiment = requiment;
	}
	/**
	 * @return the include
	 */
	public String getInclude() {
		return include;
	}
	/**
	 * @param include the include to set
	 */
	public void setInclude(String include) {
		this.include = include;
	}
	
	
}
